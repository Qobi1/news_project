def _merge_hub_seo() -> Dict[str, Dict[str, Any]]:
    out = {k: dict(v) for k, v in _HUB_SEO_DEFAULTS.items()}
    override_path = Path(__file__).resolve().parent / "hub_seo_overrides.json"
    if override_path.is_file():
        try:
            extra = json.loads(override_path.read_text(encoding="utf-8"))
            for hid, fields in extra.items():
                if hid in out and isinstance(fields, dict):
                    out[hid].update(fields)
        except (json.JSONDecodeError, OSError):
            pass
    return out


HUB_SEO: Dict[str, Dict[str, Any]] = _merge_hub_seo()


def hub_ids() -> List[str]:
    return list(HUB_ORDER)


def is_hub_id(s: str) -> bool:
    return s in HUB_FILTERS


def categories_for_hub(hub_id: str) -> List[str]:
    """Distinct DB category values covered by this hub (union query)."""
    if hub_id not in HUB_FILTERS:
        return []
    return list(dict.fromkeys(HUB_FILTERS[hub_id].values()))


def resolve_hub_filter_category(hub_id: str, filter_slug: Optional[str]) -> Optional[str]:
    """Single category for ?filter=slug, or None if invalid."""
    if not filter_slug or hub_id not in HUB_FILTERS:
        return None
    return HUB_FILTERS[hub_id].get(filter_slug)


def category_to_hub_url() -> Dict[str, str]:
    """
    Maps exact Event.category (case-insensitive key) -> path+query for internal links.
    Later hubs in HUB_ORDER overwrite earlier on duplicate category strings.
    """
    out: Dict[str, str] = {}
    for hid in HUB_ORDER:
        for slug, ru in HUB_FILTERS[hid].items():
            key = ru.strip().lower()
            out[key] = f"/{hid}/?filter={slug}"
    return out


def hub_manifest_payload() -> Dict[str, Any]:
    return {
        "byCategory": category_to_hub_url(),
        "hubs": [{"id": hid, "path": f"/{hid}/"} for hid in HUB_ORDER],
        "excluded": sorted(EXCLUDED_SEO_CATEGORIES),
    }
